void solve() {
    int a , b; cin >> a >> b;
    /*
        gcd(a , b) = gcd(b , a mod b)
        base : b = 0 => gcd(a , b) = a
    */
    function<int(int , int)> gcd = [&](int a ,int b) -> int {
        if (b == 0) return a; 
        return gcd(b , a % b);
    };
    cout << gcd(a , b);
}


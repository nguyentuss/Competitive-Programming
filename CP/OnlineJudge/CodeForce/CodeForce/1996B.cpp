#include<bits/stdc++.h> 
using namespace std;
#define int long long
const long long mod = 1e9+7;
    
void solve(){ 
    int n,k; cin >> n >> k;
    vector<vector<char>> a(n + 1 , vector<char>(n + 1)); 
    for(int i = 0;i<n;i++){
        for(int j=0;j<n;j++){
            cin >> a[i][j];
        }
    }
    vector<string> ans;
    for(int i=0;i<n;i++){
        string s = "";
        bool stat = false;
        for(int j=0;j<n;j++){
            if(i%k==0 && j%k==0) {
                s += a[i][j];
                stat = true;
            }
        }
        if (stat) {
            ans.push_back(s);
        }
    }
    for (auto x : ans) {
        cout << x << '\n';
    }
}
 


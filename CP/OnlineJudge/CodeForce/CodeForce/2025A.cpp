const int inf = 1e9;

void solve() {
    string s; cin >> s;
    string t; cin >> t;
    int ans = sz(t) + sz(s);
    int i = 0;
    while (i < min(sz(t) , sz(s)) && s[i] == t[i]) i++;
    cout << ans - i + (i > 0) << '\n';
}

signed main() {
    ios::sync_with_stdio(0); cin.tie(0);
    cin.exceptions(cin.failbit);
    int test; 
    test = 1;
    cin >> test;
    while (test--) {
        solve();
    }

}
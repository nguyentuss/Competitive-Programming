struct Tree {
    typedef int T;
    static constexpr T unit = 0;
    T f(T a, T b) { return (a + b); } // (any associative fn)
    vector<T> s; int n;
    Tree(int n = 0, T def = unit) : s(2*n, def), n(n) {}
    void update(int pos, T val) {
        for (s[pos += n] = val; pos /= 2;)
            s[pos] = f(s[pos * 2], s[pos * 2 + 1]);
    }
    T query(int b, int e) { // query [b, e)
        T ra = unit, rb = unit;
        for (b += n, e += n; b < e; b /= 2, e /= 2) {
            if (b % 2) ra = f(ra, s[b++]);
            if (e % 2) rb = f(s[--e], rb);
        }
        return f(ra, rb);
    }
};

void solve() {
    int n; 
    int cnt = 0;
    while (cin >> n && n) {
        Tree st(n);
        for (int i = 0 ; i < n ; i++) {
            int x; cin >> x;
            st.update(i , x);
        }
        if (cnt > 0) cout << '\n';
        cout << "Case " << ++cnt << ":\n"; 
        string ask;
        while (cin >> ask && ask != "END") {
            if (ask == "S") {
                int x , r; cin >> x >> r;
                x--;
                st.update(x , r);
            }   
            else {
                int x , y; cin >> x >> y;
                x--; y--; 
                cout << st.query(x , y + 1) << '\n';
            }
        }
    }
}

